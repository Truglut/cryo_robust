def generate_reconstruction_section(
    snr_reports: dict[float, EvaluationReport],
    output_path: Path,
    figures_path: Path,
    dpi: int = 150,
) -> str:
    """
    Generate the reconstruction metrics section of the LaTeX report.

    Produces one subsection per SNR level with a formatted metrics table,
    followed by a 'metrics vs. SNR' subsection containing plots for RMSE,
    Pearson correlation, and FRC resolution.

    Parameters
    ----------
    snr_reports : dict[float, EvaluationReport]
        Dict mapping every SNR level to its evaluation report.
    output_path : Path
        Path to the directory where ``report.tex`` will be generated.
        Used to compute relative figure paths.
    figures_path : Path
        Directory where generated figure files will be saved.
    dpi : int, optional
        Resolution for saved figures, by default 150.

    Returns
    -------
    str
        LaTeX text for the reconstruction section, ready to be written
        into the document.
    """
    if len(snr_reports) == 0:
        return ""

    text = "\n\\section{Reconstruction metrics}\n"

    reconstruction_dfs: dict[float, pd.DataFrame] = {}
    for snr, report in snr_reports.items():
        text += f"\n\\subsection{{SNR {snr:.3f}}}\n"
        text += create_reconstruction_table(
            report, caption=f"Reconstruction metrics for each method at SNR {snr:.3f}"
        )

        df = report.reconstruction_metrics_dataframe()
        df["snr"] = snr
        reconstruction_dfs[snr] = df

        frc_thresholds = report.frc_thresholds

    overall_rec_df = pd.concat(reconstruction_dfs.values())

    text += "\n\\subsection{Reconstruction metrics vs. SNR graphs}\n"

    snr_vs_rmse_plot = plot_vs_snr(
        df=overall_rec_df,
        metrics=["rmse"],
        save_path=figures_path / "snr_vs_rmse.pdf",
        metric_labels=["RMSE"],
        dpi=dpi,
        title="RMSE vs SNR",
        ylabel="RMSE",
    ).relative_to(output_path)

    text += "\n\\textbf{RMSE}\n"
    text += create_figure_block(
        snr_vs_rmse_plot, caption="Reconstruction RMSE vs SNR", width="0.8\\textwidth"
    )

    snr_vs_corr_plot = plot_vs_snr(
        df=overall_rec_df,
        metrics=["pearson_corr"],
        save_path=figures_path / "snr_vs_corr.pdf",
        metric_labels=["Correlation"],
        dpi=dpi,
        title="Reconstruction correlation vs SNR",
        ylabel="Correlation",
    ).relative_to(output_path)

    text += "\n\\textbf{Correlation}\n"
    text += create_figure_block(
        snr_vs_corr_plot,
        caption="Correlation with ground truth vs SNR",
        width="0.8\\textwidth",
    )

    snr_vs_gt_frc_plot = plot_vs_snr(
        df=overall_rec_df,
        metrics=["GT Resolution" + f"({thr.value})" for thr in frc_thresholds],
        save_path=figures_path / "snr_vs_gt_frc.pdf",
        metric_labels=[f"Resolution ({thr.value})" for thr in frc_thresholds],
        dpi=dpi,
        title="Ground truth reconstruction resolution vs SNR",
        ylabel="Resolution",
    ).relative_to(output_path)

    text += "\n\\textbf{Ground truth FRC}\n"
    text += "\n\\textbf{Resolution}\n"
    text += create_figure_block(
        snr_vs_gt_frc_plot,
        caption="FRC resolution vs SNR (comparing global average to ground truth)",
        width="0.8\\textwidth",
    )

    text += "\n\\textbf{AUFRC}\n"
    snr_vs_gt_aufrc_plot = plot_vs_snr(
        df = overall_rec_df,
        metrics=["AUFRC (GT)"],
        save_path=figures_path / "snr_vs_gt_aufrc.pdf",
        metric_labels=[""],
        title="Area under the ground-truth FRC curve vs. SNR",
        ylabel="AUFRC",
    ).relative_to(output_path)
    text += create_figure_block(
        snr_vs_gt_aufrc_plot,
        caption="Area under the ground-truth FRC curve vs. SNR",
        width="0.8\\textwidth"
    )

    snr_vs_hs_frc_plot = plot_vs_snr(
        df=overall_rec_df,
        metrics=["HS Resolution" + f"({thr.value})" for thr in frc_thresholds],
        save_path=figures_path / "snr_vs_hs_frc.pdf",
        metric_labels=[f"Resolution ({thr.value})" for thr in frc_thresholds],
        dpi=dpi,
        title="Half-set reconstruction resolution vs SNR",
        ylabel="Resolution",
    ).relative_to(output_path)

    text += "\n\\textbf{Half-set FRC}\n"
    text += "\n\\textbf{Resolution}\n"
    text += create_figure_block(
        snr_vs_hs_frc_plot,
        caption="FRC resolution vs SNR (comparing half-set averages)",
        width="0.8\\textwidth",
    )

    text += "\n\\textbf{AUFRC}\n"
    snr_vs_hs_aufrc_plot = plot_vs_snr(
        df = overall_rec_df,
        metrics=["AUFRC (HS)"],
        save_path=figures_path / "snr_vs_hs_aufrc.pdf",
        metric_labels=[""],
        title="Area under the half-set FRC curve vs. SNR",
        ylabel="AUFRC",
    ).relative_to(output_path)
    text += create_figure_block(
        snr_vs_hs_aufrc_plot,
        caption="Area under the half-set FRC curve vs. SNR",
        width="0.8\\textwidth"
    )

    return text


def get_classification_table(
    classification_df: pd.DataFrame, space: Space, strategy: AggregationStrategy
) -> pd.DataFrame | None:
    """
    Extract a classification metrics sub-table for a given space and strategy.

    Filters ``classification_df`` to rows matching ``space`` and
    ``strategy``, and returns ``None`` when the result would be empty or
    consist solely of the baseline average method.

    Parameters
    ----------
    classification_df : pd.DataFrame
        Full classification metrics dataframe from
        ``EvaluationReport.classification_metrics_dataframe()``.
    space : Space
        Reconstruction space to filter on (matched against the ``"space"``
        column by name).
    strategy : AggregationStrategy
        Aggregation strategy to filter on (matched against the
        ``"aggregation_strategy"`` column by value).

    Returns
    -------
    pd.DataFrame | None
        Filtered dataframe, or ``None`` if no relevant rows exist.
    """
    space_rows = classification_df["space"] == space.name

    if not space_rows.any():
        return None

    space_df = classification_df[space_rows]

    # Skip sections containing only the baseline average method.
    if len(space_df.index) == 1 and space_df["method"].iloc[0] == AVERAGE_NAME:
        return None

    rows = space_df["aggregation_strategy"] == strategy.value

    if not rows.any():
        return None

    return space_df[rows]


def generate_classification_tables(report: EvaluationReport) -> str:
    """
    Generate the classification metrics section of the report for one SNR level.

    Tables are grouped by:
    - reconstruction space
    - aggregation strategy

    Empty groups and groups containing only the baseline average
    method are skipped.

    Parameters
    ----------
    report : EvaluationReport
        Evaluation report containing classification metrics.

    Returns
    -------
    str
        LaTeX-formatted classification section.
    """
    classification_df = report.classification_metrics_dataframe()
    text = ""

    for space in Space:
        # Filter out spaces that have no associated methods or only the baseline average
        space_rows = classification_df["space"] == space.name
        space_df = classification_df[space_rows]
        if len(space_df.index) == 0:
            continue
        if len(space_df.index) == 1 and space_df["method"].iloc[0] == AVERAGE_NAME:
            continue

        text += f"\n\\subsubsection{{{space.label}}}\n"

        for strategy in AggregationStrategy:
            df = get_classification_table(classification_df, space, strategy)
            if df is None:
                continue

            text += f"\n\\textbf{{{strategy.label}}}\n\n" "\\smallskip\n\n"

            # Remove grouping columns before rendering the table.
            df = df.drop(["space", "aggregation_strategy"], axis=1)

            text += format_dataframe(df)

    return text


def generate_classification_section(
    snr_reports: dict[float, EvaluationReport],
    output_path: Path,
    figures_path: Path,
    dpi: int = 150,
) -> str:
    """
    Generates the complete classification section of the report, including all SNR
    subsections and metrics-vs-snr plots.

    Parameters
    ----------
    snr_reports : dict[float, EvaluationReport]
        Dict mapping every SNR level to its evaluation report.
    output_path : Path
        Path to the directory where the `report.tex` will be generated.
    figures_path : Path
        Path to the directory where the figures will be saved
    dpi : int, optional
        Resolution for saved figures, by default 150.

    Returns
    -------
    str
        LaTeX text for the classification section of the report, ready to be written
        into the document. This section contains:
            - One subsection per SNR level. Each of these subsections has a
            classification metrics table per space and aggregation strategy where
            methods have been tested.
            - One 'metrics vs. SNR' section, containing a plot of soft precision
            and soft recall (calculated according to Huang and Tagare) vs. SNR.
    """
    text = "\n\\section{Classification metrics}\n"

    classification_dfs: dict[float, pd.DataFrame] = {}
    for snr, report in snr_reports.items():
        text += f"\n\\subsection{{SNR {snr:.3f}}}\n"
        text += "\n"
        text += generate_classification_tables(report)

        classification_dfs[snr] = report.classification_metrics_dataframe()
        classification_dfs[snr]["snr"] = snr

    overall_df = pd.concat(classification_dfs.values())

    # For every space and aggregation strategy, plot precision and recall vs snr
    # and average precision vs snr
    text += "\n\\subsection{Classification metrics vs SNR}"
    for space in Space:
        space_rows = overall_df["space"] == space.name
        space_df = overall_df[space_rows]

        non_average = space_df[space_df["method"] != AVERAGE_NAME]

        if non_average.empty:
            continue

        text += f"\n\\subsubsection{{{space.label}}}\n"

        for strategy in AggregationStrategy:
            df = get_classification_table(overall_df, space, strategy)
            if df is None:
                continue

            text += f"\n\\textbf{{{strategy.label}}}\n"

            space_strategy_identifier = f"{space.name.lower()}_{strategy.value.lower()}"

            precision_and_recall_curves = plot_vs_snr(
                df,
                metrics=["soft_precision", "soft_recall_huang_tagare"],
                save_path=figures_path / f"snr_vs_pr_{space_strategy_identifier}.pdf",
                metric_labels=["Soft precision", "Soft recall"],
                dpi=dpi,
                title="Precision and recall vs SNR",
                ylabel="Score",
            ).relative_to(output_path)
            text += create_figure_block(
                precision_and_recall_curves,
                caption=f"Soft precision and soft recall vs. SNR ({space.label} - {strategy.label})",
            )

            average_precision_curves = plot_vs_snr(
                df,
                metrics="ap",
                save_path=figures_path / f"snr_vs_ap_{space_strategy_identifier}.pdf",
                metric_labels="Average precision",
                dpi=dpi,
                title="Average precision vs. SNR",
                ylabel="Average precision",
            ).relative_to(output_path)
            text += create_figure_block(
                average_precision_curves,
                caption=f"Average precision vs. SNR ({space.label} - {strategy.label})",
            )

    return text


def generate_figures_section(
    figure_paths: list[Path], caption_prefix: str, width: str = "\\textwidth"
) -> str:
    """
    Generate a LaTeX block embedding a list of figures.

    Parameters
    ----------
    figure_paths : list[Path]
        Paths to the figure files to include.
    caption_prefix : str
        Prefix used in each figure caption, e.g. `"Weight distribution"`.
    width: str
        Desired width for the figures. Must be in an appropriate format for the
        Desired width for the figures. Must be in an appropriate format for the
        \\includegraphics LaTeX command, e.g. `"\\textwidth"`.

    Returns
    -------
    str
        LaTeX string containing one `figure` environment per path.
    """
    text = ""

    for i, path in enumerate(figure_paths, start=1):
        text += create_figure_block(
            figure_path=path, caption=f"{caption_prefix} {i}", width=width
        )

    return text


def create_figure_block(
    figure_path: Path, caption: str = "", width: str = "\\textwidth"
):
    """
    Generate a LaTeX `figure` environment for a single image.

    Parameters
    ----------
    figure_path : Path
        Path to the figure file to include.
    caption : str, optional
        Caption text displayed below the figure. Defaults to an empty string.
    width : str, optional
        Desired width for the figure, formatted for the LaTeX
        ``\\includegraphics`` command, e.g. ``"\\textwidth"`` or ``"0.8\\textwidth"``.
        Defaults to ``"\\textwidth"``.

    Returns
    -------
    str
        LaTeX string containing a complete `figure` environment with the
        specified image and caption.
    """
    return (
        f"\n\\begin{{figure}}[H]\n"
        f"  \\centering\n"
        f"  \\includegraphics[width={width}]{{{figure_path.as_posix()}}}\n"
        f"  \\caption{{{caption}}}\n"
        f"\\end{{figure}}\n"
    )


def generate_figures_grid(
    figure_paths: list[Path],
    captions: list[str],
    figures_per_row: int,
    width: str | None = None,
) -> str:
    """
    Generate a LaTeX block embedding a list of figures arranged in rows.

    Each row uses a ``subfigure``-based layout inside a single ``figure``
    environment.  The last row is centred automatically even when it contains
    fewer figures than the others.

    Parameters
    ----------
    figure_paths : list[Path]
        Paths to the figure files to include.
    captions : list[str]
        Captions for each of the figures in `figure_paths`.
        `captions[i]` will be the caption for `figure_paths[i]`.
    figures_per_row : int
        Number of figures to place side-by-side in each row.
    width : str, optional
        Desired width for every individual figure.  Must be in a format
        accepted by ``\\includegraphics``, e.g. ``"0.45\\textwidth"``.
        Defaults to ``1/<figures_per_row>\\textwidth`` with a small gutter
        subtracted so the subfigures fit on one line.

    Returns
    -------
    str
        LaTeX string containing one ``figure`` environment per row.
    """
    if figures_per_row < 1:
        raise ValueError("figures_per_row must be at least 1")
    if len(figure_paths) != len(captions):
        raise ValueError("figure_paths and captions must be the same length")

    if width is None:
        # Leave ~0.02 of \textwidth as gutter between each pair of neighbours.
        unit = round(1 / figures_per_row - 0.02, 4)
        width = f"{unit}\\textwidth"

    text = ""

    for i in range(0, len(figure_paths), figures_per_row):
        paths_row = figure_paths[i : i + figures_per_row]
        captions_row = captions[i : i + figures_per_row]

        text += "\n\\begin{figure}[H]\n  \\centering\n"

        for path, caption in zip(paths_row, captions_row):
            text += (
                f"  \\begin{{subfigure}}[t]{{{width}}}\n"
                f"    \\centering\n"
                f"    \\includegraphics[width=\\textwidth]{{{path.as_posix()}}}\n"
                f"    \\caption{{{caption}}}\n"
                f"  \\end{{subfigure}}\n"
            )

        text += "\\end{figure}\n"

    return text


def weights_and_frc_plots_latex(
    saved_figures: dict[str, list[Path]], output_path: Path
) -> str:
    """
    Generate a complete LaTeX plots subsection from saved figure paths.

    Parameters
    ----------
    saved_figures : dict[str, list[Path]]
        Mapping returned by `save_report_figures`, with keys
        `"weight_distributions"` and `"frc_curves"`.
    output_path: Path
        Path to the directory that contains the `report.tex` file.

    Returns
    -------
    str
        LaTeX section string ready to be written into a document.
    """
    text = ""
    weight_paths = [
        p.relative_to(output_path)
        for p in saved_figures.get("weight_distributions", [])
    ]
    if weight_paths:
        text += "\n\\subsubsection{Weight Distributions}\n"
        text += generate_figures_section(weight_paths, "Weight distribution")

    frc_paths = [
        p.relative_to(output_path) for p in saved_figures.get("frc_curves", [])
    ]
    if frc_paths:
        text += "\n\\subsubsection{FRC Curves}\n"
        text += generate_figures_section(frc_paths, "FRC curves")

    return text


def generate_plots_section(
    snr_reports: dict[float, EvaluationReport],
    output_path: Path,
    figures_path: Path,
    plot_options: dict[str, Any],
) -> str:
    """
    Generates the LaTeX text for the plots section.

    Parameters
    ----------
    snr_reports : dict[float, EvaluationReport]
        Dict mapping every SNR level to its evaluation report
    output_path : Path
        Path to the directory where the `report.tex` will be generated.
    figures_path : Path
        Path to the directory where the figures will be saved.
    plot_options : dict[str, Any]
        Dict containing the following keyword arguments for figure generation:
            - max_subplots: int
            - density: bool
            - dpi: int

    Returns
    -------
    str
        LaTeX text for the 'diagnostic plots' section. This section contains one
        subsection per SNR level. Each of these subsection contains:
            - Weight distribution histograms, for each method, space and aggregation strategy.
            - One plot representing the FRC curves for all methods.
    """
    plots = save_snr_reports_figures(
        snr_reports, output_path=figures_path, **plot_options
    )

    text = "\n\\section{Diagnostic plots}\n"

    for snr in snr_reports:
        text += f"\n\\subsection{{SNR {snr:.3f}}}\n"
        text += weights_and_frc_plots_latex(
            saved_figures=plots[snr], output_path=output_path
        )

    return text


